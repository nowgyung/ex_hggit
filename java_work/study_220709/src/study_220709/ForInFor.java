package study_220709;

public class ForInFor {
	public static void main(String[] args) {
		for(int i=0; i<3; i++) {
			System.out.println("---------");
			for(int j=0; j<3; j++) {
				System.out.print("["+i+","+j+"]");
			}
			System.out.print('\n');
			//for문이 진행되는 구간을 이해하고 \n을 집어넣어야 한다

			
		}
		
	}
}
