public class EX01 {
	public static void main(String[] args) {
		number(3, 5);
		
	}
	public static void number(int a, int b) {
		System.out.println("a + b = "+ (a+b));
		System.out.println("a - b = "+ (a-b));
		System.out.println("a * b = "+ (a*b));
		System.out.println("a / b = "+ (a/b)+".."+(a%b));
		
		
	}
}
